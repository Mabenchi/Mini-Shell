int	ft_isdilimiter(int c)
{
	if (c == '>' || c == '<' || c == ' ' || c == '\0')
		return (1);
	else
		return (0);
}

int	ft_isdilimiter2(int c)
{
	if (c == '/' || c == '.' || c == ',' || c == '\'' || c == '"')
		return (1);
	else
		return (0);
}

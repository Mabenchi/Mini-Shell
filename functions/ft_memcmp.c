#include "../includes/libft.h"

int	ft_memcmp(char *s1, char *s2, int j)
{
	int			i;

	i = 0;
	while (s1[i] == s2[i] && i < j)
		i++;
	if (i == j)
		return (0);
	return (s1[i] - s2[i]);
}
